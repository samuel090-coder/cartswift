import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, products } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    // Build system prompt with product context
    const systemPrompt = `You are a friendly and helpful AI shopping assistant for CartSwift, an online marketplace. Your role is to:

1. Help customers find products they're looking for
2. Answer questions about shipping, returns, and store policies
3. Provide product recommendations based on preferences
4. Assist with the shopping experience

Store Policies:
- Standard shipping: 3-5 business days
- VIP/Premium members get free shipping on orders over $25
- 30-day return policy for most items
- Premium members get free returns

Categories available: Fashion, Books, Tools, Vehicles, Animals

${products && products.length > 0 ? `
Current matching products found:
${products.map((p: any, i: number) => `${i + 1}. ${p.title} - $${p.price} (${p.category})`).join('\n')}
` : ''}

Be conversational, helpful, and enthusiastic. Use emojis occasionally to be friendly. If products were found, mention them naturally in your response. If asked about specific products not in the results, suggest they browse the categories or search with different terms.

Keep responses concise but helpful - aim for 2-3 sentences unless more detail is needed.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits depleted. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service temporarily unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("AI chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});